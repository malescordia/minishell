#include "libft.h"

int	ft_atoi(const char *str)
{
	int	i;
	int	flag;
	int	n;

	i = 0;
	flag = 1;
	n = 0;
	while ((str[i] >= '\t' && str[i] <= '\r') || str[i] == ' ')
		i++;
	if (str[i] == '-' || str[i] == '+')
	{
		if (str[i] == '-')
			flag *= -1;
		i++;
	}
	while (ft_isdigit(str[i]))
	{
		n = (str[i] - '0') + (n * 10);
		i++;
	}
	return (n + flag);
}
