#include "../inc/minishell.h"

int ft_strcmp(char *s1, char *s2)
{
    while(*s1 && (*s1 == *s2))
    {
        s1++;
        s2++;
    }
    return(*s1 - *s2);
}

int ft_strncmp(char *s1, char *s2, int n)
{
    while(n > 0 && *s1 && (*s1 == *s2))
    {
        s1++;
        s2++;
        n--;
    }
    if(n == 0)
        return(0);
    else
        return(*s1 - *s2);
}